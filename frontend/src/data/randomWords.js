export const ALL_WORDS = Object.values(WORD_CATEGORIES).flat();

/**
 * Sample `count` entries without replacement.
 * Each result initially comes from a different category.
 */
export function sampleWords(count = 4) {
  const categories = Object.values(WORD_CATEGORIES);
  const picked = [];

  // Fisher–Yates shuffle avoids the bias of sort(() => Math.random() - 0.5).
  const shuffledCategories = [...categories];

  for (let i = shuffledCategories.length - 1; i > 0; i -= 1) {
    const randomIndex = Math.floor(Math.random() * (i + 1));

    [shuffledCategories[i], shuffledCategories[randomIndex]] = [
      shuffledCategories[randomIndex],
      shuffledCategories[i],
    ];
  }

  // First pass: draw one entry from each category.
  for (const category of shuffledCategories) {
    if (picked.length >= count) break;

    const randomIndex = Math.floor(Math.random() * category.length);
    picked.push(category[randomIndex]);
  }

  // Second pass: only needed if count exceeds the number of categories.
  while (picked.length < count && picked.length < ALL_WORDS.length) {
    const word = ALL_WORDS[Math.floor(Math.random() * ALL_WORDS.length)];

    if (!picked.includes(word)) {
      picked.push(word);
    }
  }

  return picked;
}